import Foundation
import UIKit

extension UIColor {
    
    class func tc_white() -> (UIColor) {
        return TCColor(r: 0, g: 0, b: 0, a: 0)
    }
    
    class func tc_black() -> (UIColor) {
        return TCColor(r: 0, g: 0, b: 0, a: 0)
    }
    
    
}
